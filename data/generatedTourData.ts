// diversityWeight 기반 가중 랜덤 배틀 선택
export function getWeightedRandomBattle(excludeId?: string): Battle {
  const filtered = generatedBattles.filter(b => b.id !== excludeId)
  const candidates = filtered.length > 0 ? filtered : generatedBattles

  const weights = candidates.map(b =>
    Math.max(b.leftCulture.diversityWeight, b.rightCulture.diversityWeight)
  )
  const totalWeight = weights.reduce((a, b) => a + b, 0)
  let random = Math.random() * totalWeight

  for (let i = 0; i < candidates.length; i++) {
    random -= weights[i]
    if (random <= 0) return candidates[i]
  }

  return candidates[0]
}
